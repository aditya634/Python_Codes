# Tuple
# A tuple are like lists, but unlike lists we cannot modify them.

my_tuple = (1,2,3,4,5)
print(5 in my_tuple)
user = {
(1,2) : [1,2,3],
'greet': 'hello',
'age': 20
}
print(user[(1,2)])

